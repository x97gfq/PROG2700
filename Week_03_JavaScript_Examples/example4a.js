
// Assignment Operators​

var a = 2;

// Comparison Operators​

if (a <= 2) {
    alert("a is less than or equal to 2");
}

if (a != 3) {
    alert("a is not equal to 3");
}

if (a == 2) {
    alert("a is equal to 2");
}

// Logical Operators​

if (a < 10 && a > 0) {
    alert("a is less than 10 and greater than 0");
}

if (a == 10 || a == 0) {
    alert("a is equal to 10 or a is equal to zero");
}

